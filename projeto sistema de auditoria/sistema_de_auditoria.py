@auditor
def calcular_orcamento(estrutura, *departamentos_ignorados, **config_cambio):
    def recursao(dados):
        soma = 0
        if isinstance(dados, dict):
            for chave, valor in dados.items():
                if chave in departamentos_ignorados:
                    continue
                if isinstance(valor, dict):
                    soma += recursao(valor)
                elif isinstance(valor, (int, float)):
                    soma += valor
        return soma

    valor_base = recursao(estrutura)

    taxa = config_cambio.get("taxa_cambio", 1.0)
    moeda = config_cambio.get("moeda_destino", "BRL")

    valor_final = valor_base * taxa
    return valor_final, moeda

resultado, moeda = calcular_orcamento(
    setores_da_empresa,
    "Suporte", "Treinamento",
    moeda_destino="USD", taxa_cambio=0.20
)

print(f"O orçamento auditado (ajustado) é: {moeda} {resultado:,.2f}")