import time
from functools import wraps

def auditor(func):
    @wraps(func)
    def wrapper(*args, **kwargs):
        print("="*50)
        print("RELATÓRIO DE AUDITORIA DE RECURSOS CORPORATIVOS")
        print(f"Iniciando cálculo... Argumentos: {args} {kwargs}")
        print("="*50)

        inicio = time.perf_counter()
        resultado = func(*args, **kwargs)
        fim = time.perf_counter()

        print(f"\nCálculo finalizado em: {fim - inicio:.6f} segundos.")
        print("="*50)
        return resultado
    return wrapper

